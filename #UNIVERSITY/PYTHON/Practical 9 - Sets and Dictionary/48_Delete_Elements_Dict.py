# 48. Program to delete elements from a dictionary

student = {"name": "John", "age": 20, "city": "New York"}
print("Original:", student)

del student["age"]
print("After deleting age:", student)

val = student.pop("city")
print(f"Popped value: {val}")
print("Final dictionary:", student)

