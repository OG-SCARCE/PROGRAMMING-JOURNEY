# 44. Program using set methods

my_set = {1, 2, 3}
print("Original:", my_set)

my_set.add(4)
print("After add(4):", my_set)

my_set.remove(2)
print("After remove(2):", my_set)

my_set.discard(10) # No error if not present
print("After discard(10):", my_set)

my_set.clear()
print("After clear:", my_set)

